  WHERE 
    ID IS NOT NULL AND
    NAME IS NOT NULL AND
    AGE IS NOT NULL AND
    HEALTH_STATUS IS NOT NULL AND
    ANIMAL_SPECIE IS NOT NULL
) ap
LEFT JOIN ANIMAL_SPECIE s
  ON s.SPECIE_NAME = ap.ANIMAL_SPECIE
WHERE ap.rn = 1
  AND NOT EXISTS (
    SELECT 1 FROM ANIMAL an WHERE an.ID_ANIMAL = TO_NUMBER(ap.ID)
  );

