--INSERT INTO ANIMAL FROM ANIMALS_PRODUCTS.CSV
INSERT INTO ANIMAL (
  ID_ANIMAL,
  NAME_ANIMAL,
  AGE,
  ID_ANIMAL_SPECIE,
  HEALTH,
  ID_BARN,
  QUANTITY
)
SELECT
  TO_NUMBER(ap.ID) AS ID_ANIMAL,
  ap.NAME,
  TO_NUMBER(ap.AGE),
  s.ID_ANIMAL_SPECIE, 
  ap.HEALTH_STATUS,
  CASE 
    WHEN ap.ID_BARN_BUILDING IS NOT NULL THEN TO_NUMBER(ap.ID_BARN_BUILDING)
    ELSE NULL
  END AS ID_BARN,
  1 AS QUANTITY
FROM (
  SELECT 
    ID,
    NAME,
    AGE,
    HEALTH_STATUS,
    ANIMAL_SPECIE,
    ID_BARN_BUILDING,
    ROW_NUMBER() OVER (PARTITION BY ID ORDER BY ROWNUM) AS rn
